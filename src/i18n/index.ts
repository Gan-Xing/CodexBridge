export interface Translator {
  locale: SupportedLocale;
  t(key: string, params?: MessageParams): string;
}

export function normalizeLocale(value: unknown): SupportedLocale {
  const normalized = String(value ?? '').trim().toLowerCase();
  if (normalized === 'en' || normalized === 'en-us') {
    return 'en';
  }
  if (normalized === 'zh' || normalized === 'zh-cn' || normalized === 'zh-hans') {
    return 'zh-CN';
  }
  return DEFAULT_LOCALE;
}

export function resolveLocale(value: unknown = process.env.CODEXBRIDGE_LOCALE): SupportedLocale {
  return normalizeLocale(value);
}

export function createI18n(locale: unknown = process.env.CODEXBRIDGE_LOCALE): Translator {
  const resolved = resolveLocale(locale);
  return {
    locale: resolved,
    t(key: string, params: MessageParams = {}) {
      return translate(resolved, key, params);
    },
  };
}

export function translate(locale: SupportedLocale, key: string, params: MessageParams = {}): string {
  const entry = CATALOGS[locale][key] ?? CATALOGS[DEFAULT_LOCALE][key];
  if (!entry) {
    return key;
  }
  const raw = typeof entry === 'function' ? entry(params) : entry;
  return raw.replace(/\{(\w+)\}/gu, (_, name: string) => String(params[name] ?? `{${name}}`));
}

export function formatRelativeTimeLocalized(value: unknown, locale: SupportedLocale, now = Date.now()): string {
  const updatedAt = normalizeEpochMs(value);
  if (!Number.isFinite(updatedAt) || updatedAt <= 0) {
    return translate(locale, 'common.unknown');
  }
  const diffMs = Math.max(0, now - updatedAt);
  const diffSeconds = Math.floor(diffMs / 1000);
  if (diffSeconds < 60) {
    return translate(locale, 'relative.justNow');
  }
  const diffMinutes = Math.floor(diffSeconds / 60);
  if (diffMinutes < 60) {
    return translate(locale, 'relative.minutesAgo', { count: diffMinutes });
  }
  const diffHours = Math.floor(diffMinutes / 60);
  if (diffHours < 48) {
    return translate(locale, 'relative.hoursAgo', { count: diffHours });
  }
  const diffDays = Math.floor(diffHours / 24);
  if (diffDays < 30) {
    return translate(locale, 'relative.daysAgo', { count: diffDays });
  }
  return new Date(updatedAt).toISOString().slice(0, 10);
}

function normalizeEpochMs(value: unknown): number {
  const numeric = Number(value || 0);
  if (!Number.isFinite(numeric) || numeric <= 0) {
    return 0;
  }
  return numeric < 10_000_000_000 ? numeric * 1000 : numeric;
}
